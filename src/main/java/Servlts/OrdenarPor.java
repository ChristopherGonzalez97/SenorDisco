    /**
     * Processes requests for both HTTP <code>GET</code> and <code>POST</code>
     * methods.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    protected void processRequest(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        response.setContentType("text/html;charset=UTF-8");
        try (PrintWriter out = response.getWriter()) {
            /* TODO output your page here. You may use following sample code. */
            EntityManagerFactory emf ;
            emf = Persistence.createEntityManagerFactory("cl.senordelosdiscos_SenorDisco_war_1.0-SNAPSHOTPU");
            EntityManager em = emf.createEntityManager();
            HttpSession s = request.getSession();
            int buscar = Integer.parseInt(request.getParameter("txtOrden"));
            
            List<Producto> productos = em.createNamedQuery("Producto.findAll").getResultList();
            
            switch(buscar)
            {
                case 1:
                    productos= em.createNamedQuery("Producto.OrderByMayorPrecio").getResultList();
                    break;
                case 2:
                    productos= em.createNamedQuery("Producto.OrderByMenorPrecio").getResultList();
                    break; 
                case 3:
                    productos= em.createNamedQuery("Producto.OrderByNombre").getResultList();
                    break;
                case 4:
                    productos= em.createNamedQuery("Producto.OrderStock").getResultList();
                    break;    
                        
            }
            s.setAttribute("resultado", productos);
            response.sendRedirect("Paginas/VerProductos.jsp");
        }
    }

